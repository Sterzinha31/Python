# Variáveis em python
# Não há necessidade de declará-las no início do programa
# Basta criar no meio do algoritmo com a atribuição 

teste = "Stephanie"
print(teste) # Imprime a string "Stephanie"
print (type(teste)) # Imprime o tipo da variável, string

teste = 10
print(teste) # Imprime o número 10
print (type(teste)) # Imprime o tipo da variável, que é int

teste = 1.74
print(teste) # Imprime o número 1.74
print (type(teste)) # Imprime o tipo da variável, que é float

teste = True
print(teste) # Imprime o valor True
print (type(teste)) # Imprime o tipo da variável, que é bool