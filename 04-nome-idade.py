# Atribuição de variáveis 
nome = "Stephanie"
idade = 16
altura = 1.69

# Exibir mensagem de tela
print("nome:", nome, " idade:", idade, " altura:", altura)
# Formatação com f-string
print(f"nome:{nome} idade:{idade} altura:{altura}")

